package se.l4.dust.core.internal.expression.model;

/**
 * Person representation for tests.
 * 
 * @author Andreas Holstenson
 *
 */
public class Person
{
	private String name;
	private int age;
	
	public Person()
	{
	}
	
	public String getName()
	{
		return name;
	}
	
	public void setName(String name)
	{
		this.name = name;
	}
	
	public int getAge()
	{
		return age;
	}
	
	public void setAge(int age)
	{
		this.age = age;
	}
}
